#include <unistd.h>

void	ft_print_table(char tabla[6][6])
{
	int	i;
	int j;

	i = 1;
	j = 1;
	while (i < 5)
	{
		while (j < 5)
		{
			write(1, &tabla[i][j], 1);
			write(1, &" ", 1);
			j++;
		}
		write(1, &"\n", 1);
		j = 1;
		i++;
	}
}
